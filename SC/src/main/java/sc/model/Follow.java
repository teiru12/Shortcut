package sc.model;

public class Follow {

	private int FOLLOWIDX;
	private String ID;
	private String FOLLOWID;
	
	public int getFOLLOWIDX() {
		return FOLLOWIDX;
	}
	public void setFOLLOWIDX(int fOLLOWIDX) {
		FOLLOWIDX = fOLLOWIDX;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getFOLLOWID() {
		return FOLLOWID;
	}
	public void setFOLLOWID(String fOLLOWID) {
		FOLLOWID = fOLLOWID;
	}
}
