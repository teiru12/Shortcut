package sc.model;

public class Alarm {

	private int ARARMIDX;
	private String ID;
	private String FOLLOWID;
	private String TYPE;
	private int IDX;
	
	public int getARARMIDX() {
		return ARARMIDX;
	}
	public void setARARMIDX(int aRARMIDX) {
		ARARMIDX = aRARMIDX;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getFOLLOWID() {
		return FOLLOWID;
	}
	public void setFOLLOWID(String fOLLOWID) {
		FOLLOWID = fOLLOWID;
	}
	public String getTYPE() {
		return TYPE;
	}
	public void setTYPE(String tYPE) {
		TYPE = tYPE;
	}
	public int getIDX() {
		return IDX;
	}
	public void setIDX(int iDX) {
		IDX = iDX;
	}
}