package sc.model;

public class CountReport {
	private int COUNTREPORT_IDX;
	private String TYPE;
	private int IDX;
	private int COUNT;
	
	public int getCOUNTREPORT_IDX() {
		return COUNTREPORT_IDX;
	}
	public void setCOUNTREPORT_IDX(int cOUNTREPORT_IDX) {
		COUNTREPORT_IDX = cOUNTREPORT_IDX;
	}
	public String getTYPE() {
		return TYPE;
	}
	public void setTYPE(String tYPE) {
		TYPE = tYPE;
	}
	public int getIDX() {
		return IDX;
	}
	public void setIDX(int iDX) {
		IDX = iDX;
	}
	public int getCOUNT() {
		return COUNT;
	}
	public void setCOUNT(int cOUNT) {
		COUNT = cOUNT;
	}	
}